/*  Escribe un programa que escriba 4 líneas de texto, cada una de las cuales estará formada por los números del 1 al 5. */


package Ud3AR;

public class Ud3Ej10 {

	public static void main(String[] args) {
		for (int i =1; i<=4;i++) {
			for (int j =1;j<=5;j++) {
				System.out.print(j);
			}
			System.out.println();
		}

	}

}
