//Programa que muestre los 15 primeros números

package PracticandoCicloFor;

public class MuestraNumerosHasta {

	public static void main(String[] args) {
		
		int num =15;
		
		// Empezando por 1 incrementa de 1 en 1 hasta que i = al número introducido
		for (int i =1; i<=num;i++) {
			//mustra el mensaje (El número que hemos ido incrementando
			System.out.println(i);
		}
		

	}

}
